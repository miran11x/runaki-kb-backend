async function updateKurdish() {
  try {
    let updated = 0, notFound = 0;
    for (const u of updates) {
      const res = await pool.query(
        `UPDATE faqs SET question_ku = $1, answer_ku = $2
         WHERE LOWER(TRIM(question_en)) = LOWER(TRIM($3))
         RETURNING id`,
        [u.ku_q, u.ku_a, u.en_q]
      );
      if (res.rows.length > 0) updated++;
      else notFound++;
    }
    console.log('✅ Updated ' + updated + ' FAQs with Kurdish translations');
    if (notFound > 0) console.log('⚠️  ' + notFound + ' not matched in DB');
    process.exit(0);
  } catch (err) {
    console.error('❌ Error:', err.message);
    process.exit(1);
  }
}

